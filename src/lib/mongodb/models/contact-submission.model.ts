
import { Schema, model, models } from 'mongoose';

const ContactSubmissionSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  subject: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  status: {
    type: String,
    enum: ['new', 'pending', 'resolved', 'closed'],
    default: 'new',
  },
  created_at: {
    type: Date,
    default: Date.now,
  },
  updated_at: {
    type: Date,
    default: Date.now,
  },
});

export default models.ContactSubmission || model('ContactSubmission', ContactSubmissionSchema);
